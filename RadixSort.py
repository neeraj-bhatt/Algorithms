import random
def radix_sort(arr):
    max_num = max(arr)
    exp = 1
    while max_num // exp > 0:
        counting_sort(arr, exp)
        exp *= 10

def counting_sort(arr, exp):
    n = len(arr)
    output = [0] * n
    count = [0] * 10

    for i in range(n):
        index = arr[i] // exp
        count[index % 10] += 1

    for i in range(1, 10):
        count[i] += count[i - 1]

    for i in range(n - 1, -1, -1):
        index = arr[i] // exp
        output[count[index % 10] - 1] = arr[i]
        count[index % 10] -= 1

    for i in range(n):
        arr[i] = output[i]
# generate an array of size 30 with random integers
arr = [random.randint(0, 100) for _ in range(30)]

# print the unsorted array
print("Unsorted array:", arr)

# sort the array using radix sort
radix_sort(arr)

# print the sorted array
print("Sorted array:", arr)
